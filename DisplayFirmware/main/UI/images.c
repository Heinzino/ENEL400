// Returns a malloc'd buffer with the decoded data.
// The outputSize pointer is filled with the number of pixels written.
uint8_t* decodeRLEWithMalloc(const uint8_t *rleData, size_t rleLength, size_t *outputSize) {
    // First pass: calculate total decoded size
    size_t totalSize = 0;
    for (size_t i = 0; i < rleLength; i += 2) {
        totalSize += rleData[i + 1];  // count
    }

    // Allocate buffer
    uint8_t *decoded = (uint8_t *)malloc(totalSize);
    if (!decoded) {
        *outputSize = 0;
        return NULL; // allocation failed
    }

    // Second pass: decode into buffer
    size_t outIndex = 0;
    for (size_t i = 0; i < rleLength; i += 2) {
        uint8_t value = rleData[i];
        uint8_t count = rleData[i + 1];

        for (uint8_t j = 0; j < count; ++j) {
            decoded[outIndex++] = value;
        }
    }

    *outputSize = totalSize;
    return decoded;
}

